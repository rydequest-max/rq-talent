def adapter_rss(cfg):
    import xml.etree.ElementTree as ET
    out, timeout = [], cfg.get("request_timeout", 20)
    for url in cfg.get("rss_feeds", []):
        try:
            body = http_get(url, timeout)
            root = ET.fromstring(body)
            for item in root.iter("item"):
                def g(tag):
                    el = item.find(tag)
                    return el.text if el is not None else ""
                title = g("title")
                if not detect_country(title, g("description")):
                    continue
                out.append(normalize(
                    source="RSS",
                    title=title, company="", location="",
                    description=g("description"), url=g("link"),
                    posted=g("pubDate"), cfg=cfg,
                ))
        except Exception as e:
            print(f"  rss[{url}] error: {type(e).__name__}: {e}", file=sys.stderr)
    return out, f"RSS ({len(out)})"


def adapter_workday(cfg):
    """Workday's job API is open (no key). Big oilfield-services / energy firms use
    it. Each board = {host, tenant, site, company, type}. We page the listing,
    keep Gulf locations, then fetch each job's detail for the full JD."""
    out, timeout = [], cfg.get("request_timeout", 20)
    max_pages = cfg.get("workday_max_pages", 15)
    max_gulf = cfg.get("workday_max_per_board", 40)
    for b in cfg.get("workday_boards", []):
        host, ten, site = b.get("host"), b.get("tenant"), b.get("site")
        company = b.get("company") or ten
        ctype = b.get("type", "service")
        if not (host and ten and site):
            continue
        list_url = f"https://{host}/wday/cxs/{ten}/{site}/jobs"
        seen_paths, found = set(), 0
        # Query by target-country name AND its major cities (server-side narrows to
        # that region) — far fewer requests than paging the whole board, and catches
        # employers that list the city only (e.g. "Cleveland Clinic Abu Dhabi").
        queries = []
        for c in (cfg.get("target_countries", []) or ["United Arab Emirates"]):
            queries.append(c)
            queries.extend(CITY_QUERY.get(c, []))

        def fetch_page(off, q):
            body = json.dumps({"appliedFacets": {}, "limit": 20,
                               "offset": off, "searchText": q}).encode("utf-8")
            req = urllib.request.Request(
                list_url, data=body,
                headers={"User-Agent": UA, "Content-Type": "application/json",
                         "Accept": "application/json"})
            with urllib.request.urlopen(req, timeout=timeout) as r:
                return json.loads(r.read().decode("utf-8", "replace"))

        try:
            for q in queries:
                if found >= max_gulf:
                    break
                first = fetch_page(0, q)
                total = first.get("total", 0)
                for off in range(0, min(total, max_pages * 20), 20):
                    page = first if off == 0 else fetch_page(off, q)
                    posts = page.get("jobPostings", [])
                    if not posts:
                        break
                    for p in posts:
                        loc = p.get("locationsText", "")
                        if not detect_country(loc):
                            continue
                        ext = p.get("externalPath", "")
                        if ext in seen_paths:
                            continue
                        seen_paths.add(ext)
                        apply_url = f"https://{host}/en-US/{site}{ext}"
                        desc, posted = "", None
                        try:
                            durl = f"https://{host}/wday/cxs/{ten}/{site}{ext}"
                            info = json.loads(http_get(durl, timeout)).get("jobPostingInfo", {})
                            desc = info.get("jobDescription", "") or ""
                            posted = info.get("startDate")
                            loc = info.get("location", loc) or loc
                        except Exception:
                            pass
                        out.append(normalize(
                            source=f"Workday:{company}", title=p.get("title"),
                            company=company, location=loc, description=desc,
                            url=apply_url, posted=posted, company_type=ctype,
                            industry=b.get("industry"), cfg=cfg))
                        found += 1
                        if found >= max_gulf:
                            break
                        time.sleep(0.12)
                    if found >= max_gulf:
                        break
                    time.sleep(0.1)
        except Exception as e:
            print(f"  workday[{company}] error: {type(e).__name__}: {e}", file=sys.stderr)
    return out, f"Workday ({len(out)})"


def adapter_smartrecruiters(cfg):
    """SmartRecruiters postings API is open (no key). AECOM and others use it.
    Each board = {company, display, industry}. Filtered server-side to country=ae
    (or cfg target). We page the listing, then fetch each posting's full JD."""
    out, timeout = [], cfg.get("request_timeout", 20)
    cap = cfg.get("smartrecruiters_max_per_board", 50)
    # country codes for SR's ?country= param
    cc = {"United Arab Emirates": "ae", "Qatar": "qa", "Saudi Arabia": "sa"}
    countries = [cc[c] for c in cfg.get("target_countries", []) if c in cc] or ["ae"]
    for b in cfg.get("smartrecruiters_boards", []):
        cid = b.get("company")
        if not cid:
            continue
        disp = b.get("display") or cid
        found = 0
        for country in countries:
            if found >= cap:
                break
            try:
                offset = 0
                while found < cap:
                    lurl = (f"https://api.smartrecruiters.com/v1/companies/{cid}/postings"
                            f"?limit=100&offset={offset}&country={country}")
                    data = json.loads(http_get(lurl, timeout))
                    items = data.get("content", [])
                    if not items:
                        break
                    for it in items:
                        loc = (it.get("location") or {}).get("fullLocation") \
                            or ", ".join(filter(None, [(it.get("location") or {}).get("city"),
                                                       (it.get("location") or {}).get("region")]))
                        pid = it.get("id")
                        desc, apply_url = "", f"https://jobs.smartrecruiters.com/{cid}/{pid}"
                        try:
                            det = json.loads(http_get(
                                f"https://api.smartrecruiters.com/v1/companies/{cid}/postings/{pid}", timeout))
                            secs = det.get("jobAd", {}).get("sections", {})
                            desc = " ".join(filter(None, [
                                secs.get("jobDescription", {}).get("text", ""),
                                secs.get("qualifications", {}).get("text", "")]))
                            apply_url = det.get("postingUrl") or det.get("applyUrl") or apply_url
                        except Exception:
                            pass
                        out.append(normalize(
                            source=f"SmartRecruiters:{disp}", title=it.get("name"),
                            company=disp, location=loc, description=desc, url=apply_url,
                            posted=it.get("releasedDate"), company_type="employer",
                            industry=b.get("industry"), cfg=cfg))
                        found += 1
                        if found >= cap:
                            break
                        time.sleep(0.15)
                    if len(items) < 100:
                        break
                    offset += 100
            except Exception as e:
                print(f"  smartrecruiters[{disp}/{country}] error: {type(e).__name__}: {e}", file=sys.stderr)
    return out, f"SmartRecruiters ({len(out)})"


