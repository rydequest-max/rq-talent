#!/usr/bin/env python3
"""
RQ Talent — Gulf oil & gas / shutdown job scraper.

Runs hourly via GitHub Actions. Pulls postings from official aggregator APIs
(Jooble, Careerjet) and open ATS APIs (Greenhouse, Lever), normalizes them to a
single schema, tags shutdown/turnaround roles, dedupes, and writes ../jobs.json.

Design notes
------------
* Stdlib only (urllib, json, re, hashlib) — nothing to break in CI.
* Every source is wrapped in try/except. One source failing never aborts the run.
* Results are MERGED with the existing jobs.json, so a blocked/empty run never
  wipes the feed. Entries unseen for `retention_days` are dropped.
* Datacenter IPs (incl. GitHub runners) get blocked by HTML job boards, so we
  rely on JSON APIs that are ToS-friendly and don't block. HTML/RSS adapters are
  included as optional best-effort only.

Secrets (set as GitHub repo secrets, read from env):
    JOOBLE_API_KEY     — free key from https://jooble.org/api/about
    CAREERJET_AFFID    — free affiliate id from https://www.careerjet.com/partners/
"""

