def write_outputs(payload):
    """Write jobs.json (for fetch on GitHub Pages) AND jobs.js (a <script> that
    sets window.RQ_JOBS — works when the portal is opened locally via file://,
    where fetch() is blocked by the browser)."""
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        json.dump(payload, f, ensure_ascii=False, indent=2)
    with open(OUTPUT_JS_PATH, "w", encoding="utf-8") as f:
        f.write("/* Auto-generated by scraper/scrape.py. Do not edit by hand. */\n")
        f.write("window.RQ_JOBS = ")
        json.dump(payload, f, ensure_ascii=False)
        f.write(";\n")

