def load_existing():
    try:
        with open(OUTPUT_PATH, "r", encoding="utf-8") as f:
            data = json.load(f)
        return {j["id"]: j for j in data.get("jobs", [])}
    except Exception:
        return {}


def main():
    cfg = load_config()
    target = set(cfg.get("target_countries", []))
    print(f"RQ Talent scraper — {now_iso()}")

    scraped, summaries = [], []
    for adapter in ADAPTERS:
        try:
            jobs, summary = adapter(cfg)
        except Exception as e:
            jobs, summary = [], f"{adapter.__name__} crashed: {e}"
        scraped.extend(jobs)
        summaries.append(summary)
        print("  •", summary)

    # keep only target-country jobs with a usable title
    fresh = [j for j in scraped if j["title"] and (j["country"] in target)]

    existing = load_existing()
    now = now_iso()
    seen_now = set()
    for j in fresh:
        seen_now.add(j["id"])
        if j["id"] in existing:
            prev = existing[j["id"]]
            j["first_seen"] = prev.get("first_seen", now)
        else:
            j["first_seen"] = now
        j["last_seen"] = now
        existing[j["id"]] = j

    # carry forward jobs not seen this run, drop stale ones
    retention = timedelta(days=cfg.get("retention_days", 21))
    cutoff = datetime.now(timezone.utc) - retention
    merged = []
    for jid, j in existing.items():
        if jid not in seen_now:
            try:
                if datetime.fromisoformat(j.get("last_seen", now)) < cutoff:
                    continue  # stale -> drop
            except Exception:
                pass
        merged.append(j)

    # newest first by first_seen
    merged.sort(key=lambda x: x.get("first_seen", ""), reverse=True)

    industries = {}
    for j in merged:
        industries[j.get("industry", "Other")] = industries.get(j.get("industry", "Other"), 0) + 1
    payload = {
        "generated_at": now,
        "count": len(merged),
        "shutdown_count": sum(1 for j in merged if j.get("shutdown")),
        "by_country": {c: sum(1 for j in merged if j.get("country") == c) for c in target},
        "by_industry": dict(sorted(industries.items(), key=lambda kv: -kv[1])),
        "sources": summaries,
        "jobs": merged,
    }
    write_outputs(payload)
    print(f"Wrote {OUTPUT_PATH} (+ jobs.js): {len(merged)} jobs "
          f"({payload['shutdown_count']} shutdown). New this run: {len(seen_now)}.")


if __name__ == "__main__":
    main()
