def adapter_jooble(cfg):
    key = os.environ.get("JOOBLE_API_KEY", "").strip()
    if not (cfg.get("jooble_enabled") and key):
        return [], "Jooble (skipped: no JOOBLE_API_KEY)"
    out = []
    timeout = cfg.get("request_timeout", 20)
    locations = cfg.get("target_countries", []) or ["United Arab Emirates"]
    # One keyword string for the API. Prefer an explicit broad query (incl. HR) if set.
    keywords = cfg.get("jooble_query") or " OR ".join(cfg["keywords"][:8])
    pages = max(1, int(cfg.get("jooble_pages", 1)))   # each page = 1 API request
    # (query, pages) list. A dedicated narrow query (e.g. HR) guarantees those roles
    # surface instead of being crowded out of the broad query's first pages.
    queries = [(keywords, pages)]
    if cfg.get("jooble_hr_query"):
        queries.append((cfg["jooble_hr_query"], max(1, int(cfg.get("jooble_hr_pages", 1)))))
    for loc in locations:
      for qstr, qpages in queries:
        for page in range(1, qpages + 1):
          try:
            body = http_post_json(
                f"https://jooble.org/api/{key}",
                {"keywords": qstr, "location": loc, "page": str(page)},
                timeout,
            )
            data = json.loads(body)
            jobs = data.get("jobs", [])
            if not jobs:
                break  # no more results — stop paging this location
            for j in jobs:
                out.append(normalize(
                    source="Jooble",
                    title=j.get("title"), company=j.get("company"),
                    location=j.get("location") or loc,
                    description=j.get("snippet"), url=j.get("link"),
                    salary=j.get("salary") or None, posted=j.get("updated"),
                    cfg=cfg,
                ))
            time.sleep(1)
          except Exception as e:
            print(f"  jooble[{loc}] error: {type(e).__name__}: {e}", file=sys.stderr)
    return out, f"Jooble ({len(out)})"


def adapter_careerjet(cfg):
    """Careerjet v4 API (search.api.careerjet.net/v4/query). Auth = HTTP Basic with
    the API key as username and an empty password. NOTE: Careerjet requires you to
    pre-declare the server IP(s) calls come from — so this only works from a fixed
    IP (a static-IP host or proxy), NOT from GitHub Actions' ephemeral runners."""
    import base64
    key = os.environ.get("CAREERJET_API_KEY", "").strip() or os.environ.get("CAREERJET_AFFID", "").strip()
    if not (cfg.get("careerjet_enabled") and key):
        return [], "Careerjet (skipped: no CAREERJET_API_KEY)"
    out = []
    timeout = cfg.get("request_timeout", 20)
    referer = cfg.get("careerjet_referer", "https://www.rydequest.com/find-jobs/")
    max_jobs = cfg.get("careerjet_max", 100)
    auth = "Basic " + base64.b64encode((key + ":").encode()).decode()
    locale_map = {"United Arab Emirates": "en_AE", "Qatar": "en_QA", "Saudi Arabia": "en_SA"}
    targets = cfg.get("target_countries", []) or ["United Arab Emirates"]

    # Careerjet authenticates by the CALLER's IP. On GitHub Actions the runner IP is
    # ephemeral, so route through a static-IP proxy (CAREERJET_PROXY) whose IP you
    # declare in the Careerjet dashboard. e.g. http://user:pass@proxy-host:port
    proxy = os.environ.get("CAREERJET_PROXY", "").strip()
    opener = urllib.request.build_opener(
        urllib.request.ProxyHandler({"http": proxy, "https": proxy}) if proxy
        else urllib.request.ProxyHandler({}))

    def cj_get(url):
        req = urllib.request.Request(url, headers={
            "User-Agent": UA, "Authorization": auth, "Referer": referer,
            "Content-Type": "application/json"})
        with opener.open(req, timeout=timeout) as r:
            return r.read().decode("utf-8", "replace")

    for loc in targets:
        locale = locale_map.get(loc, "en_GB")
        found = 0
        for page in range(1, 6):
            if found >= max_jobs:
                break
            try:
                params = urllib.parse.urlencode({
                    "locale_code": locale, "keywords": "", "location": loc,
                    "user_ip": "11.22.33.44", "user_agent": UA,
                    "page": str(page), "page_size": "100", "sort": "date",
                })
                body = cj_get(f"https://search.api.careerjet.net/v4/query?{params}")
                data = json.loads(body)
                if data.get("type") != "JOBS":
                    break
                jobs = data.get("jobs", [])
                if not jobs:
                    break
                for j in jobs:
                    out.append(normalize(
                        source="Careerjet",
                        title=j.get("title"), company=j.get("company"),
                        location=j.get("locations") or loc,
                        description=j.get("description"), url=j.get("url"),
                        salary=j.get("salary") or None, posted=j.get("date"),
                        cfg=cfg,
                    ))
                    found += 1
                time.sleep(1)
            except Exception as e:
                print(f"  careerjet[{loc} p{page}] error: {type(e).__name__}: {e}", file=sys.stderr)
                break
    return out, f"Careerjet ({len(out)})"


def adapter_greenhouse(cfg):
    out, timeout = [], cfg.get("request_timeout", 20)
    for entry in cfg.get("greenhouse_boards", []):
        token = entry if isinstance(entry, str) else entry.get("token")
        display = token.replace("-", " ").title() if isinstance(entry, str) else (entry.get("company") or token)
        ind = None if isinstance(entry, str) else entry.get("industry")
        try:
            body = http_get(
                f"https://boards-api.greenhouse.io/v1/boards/{token}/jobs?content=true",
                timeout,
            )
            data = json.loads(body)
            for j in data.get("jobs", []):
                loc = (j.get("location") or {}).get("name", "")
                if not detect_country(loc, j.get("title", "")):
                    continue  # target region only
                out.append(normalize(
                    source=f"Greenhouse:{display}",
                    title=j.get("title"), company=display,
                    location=loc, description=j.get("content"),
                    url=j.get("absolute_url"),
                    posted=j.get("updated_at"), company_type="employer",
                    industry=ind, cfg=cfg,
                ))
            time.sleep(0.5)
        except urllib.error.HTTPError as e:
            print(f"  greenhouse[{token}] HTTP {e.code} (token may not exist)", file=sys.stderr)
        except Exception as e:
            print(f"  greenhouse[{token}] error: {type(e).__name__}: {e}", file=sys.stderr)
    return out, f"Greenhouse ({len(out)})"


