def http_get(url, timeout, headers=None):
    h = {"User-Agent": UA, "Accept": "application/json, text/xml, */*"}
    if headers:
        h.update(headers)
    req = urllib.request.Request(url, headers=h)
    with urllib.request.urlopen(req, timeout=timeout) as r:
        return r.read().decode("utf-8", "replace")


def http_post_json(url, payload, timeout):
    data = json.dumps(payload).encode("utf-8")
    req = urllib.request.Request(
        url, data=data,
        headers={"User-Agent": UA, "Content-Type": "application/json"},
    )
    with urllib.request.urlopen(req, timeout=timeout) as r:
        return r.read().decode("utf-8", "replace")


def clean_text(html):
    if not html:
        return ""
    txt = TAG_RE.sub(" ", html)
    txt = (txt.replace("&amp;", "&").replace("&lt;", "<").replace("&gt;", ">")
              .replace("&nbsp;", " ").replace("&quot;", '"').replace("&#39;", "'"))
    return WS_RE.sub(" ", txt).strip()


def detect_country(*texts):
    blob = " ".join(t for t in texts if t).lower()
    for country, pats in COUNTRY_PATTERNS.items():
        for p in pats:
            if p in blob:
                return country
    return None


def detect_city(country, *texts):
    blob = " ".join(t for t in texts if t).lower()
    if not country:
        return ""
    for p in COUNTRY_PATTERNS[country]:
        # skip the country-name aliases, keep the city-looking ones
        if p in blob and p not in (country.lower(), "uae", "u.a.e", "ksa", "k.s.a"):
            return p.title()
    return ""


def is_shutdown(title, desc):
    blob = (" " + (title or "") + " " + (desc or "") + " ").lower()
    return any(term in blob for term in SHUTDOWN_TERMS)


def extract_skills(title, desc):
    blob = (" " + (title or "") + " " + (desc or "") + " ").lower()
    found = []
    for skill in SKILL_VOCAB:
        if skill in blob:
            found.append(skill)
    # de-dupe while preserving order, cap to keep payload small
    seen, out = set(), []
    for s in found:
        if s not in seen:
            seen.add(s); out.append(s)
    return out[:25]


def detect_seniority(title):
    t = (title or "").lower()
    for label, cues in SENIORITY_MAP:
        if any(c in t for c in cues):
            return label
    return "Mid"


def find_contact(desc):
    if not desc:
        return None
    m = EMAIL_RE.search(desc)
    if not m:
        return None
    email = m.group(0)
    # skip obvious noise
    if any(x in email.lower() for x in ("example.com", "sentry", "wixpress", "no-reply", "noreply")):
        return None
    return email


def make_id(source, url, title, company):
    basis = (url or "") + "|" + (title or "") + "|" + (company or "")
    return source.split(":")[0].lower() + "_" + hashlib.sha1(basis.encode("utf-8")).hexdigest()[:14]


def normalize(*, source, title, company, location, description, url,
              salary=None, posted=None, company_type="unknown", industry=None, cfg=None):
    title = clean_text(title)
    company = clean_text(company) or "—"
    location = clean_text(location)
    desc = clean_text(description)
    maxc = (cfg or {}).get("max_description_chars", 4000)
    if len(desc) > maxc:
        desc = desc[:maxc] + " …"
    country = detect_country(location, title, desc)
    return {
        "id": make_id(source, url, title, company),
        "title": title,
        "company": company,
        "location": location or (country or ""),
        "country": country,
        "city": detect_city(country, location),
        "description": desc,
        "url": url or "",
        "source": source,
        "company_type": company_type,
        "industry": industry or classify_industry(title, desc, company),
        "salary": salary,
        "contact": find_contact(desc),
        "skills": extract_skills(title, desc),
        "seniority": detect_seniority(title),
        "shutdown": is_shutdown(title, desc),
        "posted": posted,
    }


